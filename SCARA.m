% --- Executes on button press in btn_forward.
function btn_forward_Callback(hObject, eventdata, handles)
% hObject    handle to btn_forward (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
Th_1 = str2double(handles.Theta_1.String)*pi/180;
Th_2 = str2double(handles.Theta_2.String)*pi/180;
Th_3 = str2double(get(handles.d_3,'string'));
Th_4 = str2double(handles.Theta_4.String)*pi/180;

L1=Link([0,300,250,0,0]);
L1.qlim = [-2*pi/3 2*pi/3];
L2=Link([0,0,150,pi,0]);
L2.qlim = [-13*pi/18 13*pi/18];
L3=Link([0,0,0,0,1]);
L3.qlim = [0 150];
L4=Link([0,50,0,0,0]);
L4.qlim = [-2*pi 2*pi];
SCARA = SerialLink([L1 L2 L3 L4], 'name', 'SCARA');
ws = [-5000 5000 -5000 5000 -5000 5000];
plot_options = {'workspace',ws};
SCARA.plot([Th_1 Th_2 Th_3 Th_4]); 
T = SCARA.fkine([Th_1 Th_2 Th_3 Th_4])
array = transl(T)
    
array(1)
array(2)
array(3)

set(handles.Pos_X,'string',num2str((array(1))));
set(handles.Pos_Y,'string',num2str((array(2))));
set(handles.Pos_Z,'string',num2str((array(3))));

